package com.yuanshuai.weixin.po;

import java.util.List;

public class RobotMessage {

	private String code;
	private String text;
	private String url;
	private List<RobotMessageDetail> list;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public List<RobotMessageDetail> getList() {
		return list;
	}

	public void setList(List<RobotMessageDetail> list) {
		this.list = list;
	}

}
